class ReaxmlEzrColMls_agent extends ReaxmlEzrColumn {
	const XPATH = '//agentID';
	
	/*
	 * (non-PHPdoc) @see ReaxmlDbColumn::getValue()
	 */
	public function getValue() {
		$found = $this->xml->xpath ( self::XPATH );
		if (count($found) == 0) {
			return $this->isNew() ? '' : null ;
		}
		return $found[0];
	}
}